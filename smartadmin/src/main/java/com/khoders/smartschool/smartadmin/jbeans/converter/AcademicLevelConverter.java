package com.khoders.smartschool.smartadmin.jbeans.converter;

import com.khoders.smartschool.entities.AcademicLevel;
import javax.faces.component.UIComponent;
import javax.faces.context.FacesContext;
import javax.faces.convert.FacesConverter;
import org.omnifaces.converter.SelectItemsConverter;

/**
 *
 * @author richa
 */
@FacesConverter(forClass = AcademicLevel.class)
public class AcademicLevelConverter extends SelectItemsConverter 
{
    @Override
    public String getAsString(FacesContext context, UIComponent component, Object value)
    {
        if (value == null)
        {
            return null;
        }
        return ((AcademicLevel) value).getId();
    }  
}
