package com.khoders.smartschool.smartweb.jbeans.converter;

import com.khoders.smartschool.entities.FeesType;
import javax.faces.component.UIComponent;
import javax.faces.context.FacesContext;
import javax.faces.convert.FacesConverter;
import org.omnifaces.converter.SelectItemsConverter;

/**
 *
 * @author richa
 */
@FacesConverter(forClass = FeesType.class)
public class FeesTypeConverter extends SelectItemsConverter 
{
    @Override
    public String getAsString(FacesContext context, UIComponent component, Object value)
    {
        if (value == null)
        {
            return null;
        }
        return ((FeesType) value).getId();
    }  
}
